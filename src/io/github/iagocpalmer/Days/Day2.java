	@Override
	public void run2() {
		MyFileReader fileReader = new MyFileReader("res/input_2.txt");
		
		String line;
		int result = 0;
		Pattern pattern = Pattern.compile("([0-9]+ (blue|green|red))");
		while((line=fileReader.readLine())!=null) {
			result += GetNumberOfGame(line, pattern);
		}
		
		System.out.println("Result part two: " + result);
	}
	
	private int GetNumberOfGame(String game, Pattern pattern) {
		Matcher matcher = pattern.matcher(game);
		String s = "";
		int[] currentCount = {0,0,0};
		while(matcher.find()) {
			s = matcher.group();
			String[] set = s.split(" ");
			int nCubes = Integer.parseInt(set[0]);
			int cubeColorId = colorEncoder.get(set[1]);
			currentCount[cubeColorId]=currentCount[cubeColorId]>=nCubes?currentCount[cubeColorId]:nCubes;
		}
		return currentCount[0]*currentCount[1]*currentCount[2];
	}
	
	private int IsValidGame(String game, Pattern pattern) {
		Matcher matcher = pattern.matcher(game);
		String s = "";
		while(matcher.find()) {
			s = matcher.group();
			String[] set = s.split(" ");
			int nCubes = Integer.parseInt(set[0]);
			int cubeColorId = colorEncoder.get(set[1]);
			if(nCubes>cubeCount[cubeColorId]) {
				return 0;
			}
		}
		return 1;
	}
