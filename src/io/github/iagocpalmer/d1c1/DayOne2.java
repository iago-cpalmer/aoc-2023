		String[] numberCodes = { "one", "two", "three", "four", "five", "six", "seven", "eight", "nine" };
		for (int i = 0; i < numberCodes.length; i++) {
			encoder.put(numberCodes[i], i + 1);
		}
		
		File file = new File("res/input_1_1.txt");
		int result = 0;
		BufferedReader br = new BufferedReader(new FileReader(file));
		Pattern p = Pattern.compile("(([0-9])\\w*([0-9]))|[0-9]");
		String inputLine;
		while ((inputLine = br.readLine()) != null) {
			// Convert all spelt numbers into its numeric value
			for (int i = 0; i < numberCodes.length; i++) {
				inputLine = inputLine.replaceAll(numberCodes[i],
						"" + (numberCodes[i].charAt(0) + (DigitStringToInt(numberCodes[i]) + "")
								+ numberCodes[i].charAt(numberCodes[i].length() - 1)));
			}
			Matcher matcher = p.matcher(inputLine);
			matcher.find();
			char first = inputLine.charAt(matcher.start());
			char last = inputLine.charAt(matcher.end() - 1);
			result += (((int) first - 48) * 10) + ((int) last - 48);
		}

		System.out.println("Result: " + result);
